package j0707;

public class Calculator {
// 사칙연산을 하는 메소드 4개 선언

	int plus(int num1, int num2) {
		return num1 + num2;
	}

	int minus(int num1, int num2) {
		return num1 - num2;
	}

	int multi(int num1, int num2) {
		return num1 * num2;
	}

	double divide(double num1, double num2) {
		return num1 / num2;
	}
	//
//
	//
}
